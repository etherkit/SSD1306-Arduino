void SSD1306::pixel(uint8_t x, uint8_t y, enum PIXEL_COLOR color)
{
	// Bounds checking
	if((x > OLED_COLS - 1) || (y > OLED_ROWS - 1))
	{
		return;
	}

	// Locate the correct byte to update
	uint16_t byte = x + (OLED_COLS * (y / 8));

  // Create the bit mask
  uint8_t mask = (1 << (y % 8));
  //uint8_t data = *(oled_buffer + byte);
  //data &= ~mask;

	// Now set the correct pixel in that byte
	switch(color)
	{
	case PIXEL_BLACK:
		//oled_buffer[byte] = oled_buffer[byte] & ~(1 << (y % 8));
    *(oled_buffer + byte) = *(oled_buffer + byte) & ~mask;
		break;

	case PIXEL_WHITE:
		//oled_buffer[byte] = oled_buffer[byte] | (1 << (y % 8));
    *(oled_buffer + byte) = *(oled_buffer + byte) | mask;
		break;

	default:
		break;
	}
}

void SSD1306::text(uint8_t x, uint8_t y, enum TEXT_FONT font, bool inverse, char* str)
{
	// Bounds checking

	// Figure out how many columns we have to print for each character
	uint8_t cols, rows, offset, pages;

	switch(font)
	{
	case TEXT_FONT_5X8:
		cols = 5;
		rows = 8;
		//offset = 32;
		offset = 0;
		pages = 1;
		break;

	case TEXT_FONT_11X21:
		cols = 11;
		rows = 8;
		offset = 46;
		pages = 3;
		break;

	default:
		return;
	}

	uint8_t h, i, j, chr_count = 0;
	uint8_t col_buffer;

	// Iterate thru characters
	while(*str)
	{
		// For the large font, display anything not in the char set as a space
		if(((char)(*str) < '.' || (char)(*str) > '9') && (font == TEXT_FONT_11X21))
		{
			// Iterate thru pages
			for(h = 0; h < pages; h++)
			{
				// Iterate thru the columns
				for(i = 0; i < cols; i++)
				{
					for(j = 0; j < rows; j++)
					{
						pixel(x + i + chr_count * (cols + 1), y + j + (h * 8), inverse ? PIXEL_WHITE : PIXEL_BLACK);
					}
				}
			}
		}
		else
		{
			// Iterate thru pages
			for(h = 0; h < pages; h++)
			{
				// Iterate thru the columns
				for(i = 0; i < cols; i++)
				{
					// Put each column in to the buffer
					switch(font)
					{
					case TEXT_FONT_5X8:
						col_buffer = pgm_read_byte(&font_5x8[((uint8_t)(*str - offset) * cols) + i]);
            break;

					case TEXT_FONT_11X21:
						col_buffer = pgm_read_byte(&font_11x21[((uint8_t)(*str - offset) * cols * pages) + i + (h * cols)]);
            break;
					}

					for(j = 0; j < rows; j++)
					{
						if((col_buffer >> j) & 1)
						{
							pixel(x + i + chr_count * (cols + 1), y + j + (h * 8), inverse ? PIXEL_BLACK : PIXEL_WHITE);
						}
						else
						{
							pixel(x + i + chr_count * (cols + 1), y + j + (h * 8), inverse ? PIXEL_WHITE : PIXEL_BLACK);
						}
					}
				}
			}
		}

		chr_count++;
		str++;
	}
}

