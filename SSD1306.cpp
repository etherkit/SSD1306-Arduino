void SSD1306::init(void)
{
	// Initialize SPI
	SPI.begin();

	// Set OLED control pins to outputs
	pinMode(OLED_DC, OUTPUT);
	pinMode(OLED_CS, OUTPUT);
	pinMode(OLED_RESET, OUTPUT);

	// Set DC to low and CS, RES to high
	digitalWrite(OLED_DC, LOW);
	digitalWrite(OLED_CS, HIGH);
	digitalWrite(OLED_RESET, HIGH);

	// Pulse RESET low
  delay(0.1);
  digitalWrite(OLED_RESET, LOW);
  delay(0.1);
  digitalWrite(OLED_RESET, HIGH);
	delay(0.1);

	// Display off
	send_command(0xAE);

	// Set DCLK ratio and CLK freq
	send_command(0xD5);
	send_command(0x80);

	// Set multiplex ratio for 64 lines
	send_command(0xA8);
	send_command(0x3F);

	// Set display offset
	send_command(0xD3);
	send_command(0x00);

	// Remap segment 127 to 0
	send_command(0xA1);

	// Set display start line
	send_command(0x7F);

	// Set memory addressing mode to Horizontal
	send_command(0x20);
	send_command(0x00);

	// Enable charge pump
	send_command(0x8D);
	send_command(0x14);

	// Set pre-charge period
	send_command(0xD9);
	send_command(0xF1);

	// Set VCOMH deselect level
	send_command(0xDB);
	send_command(0x20);

	// Set COM output scan direction
	send_command(0xC8);

	// Set COM pins for alternate hardware configuration
	send_command(0xDA);
	send_command(0x12);

	// Set normal (white) pixels
	send_command(0xA6);

	// Clear the display and refresh the buffer
	//buffer_clear();
	buffer_write();

	// Display on
	send_command(0xAF);
}

void SSD1306::send_command(uint8_t cmd)
{
	SPI.beginTransaction(spi_config);

	// Set DC and CS low for command
	digitalWrite(OLED_DC, LOW);
	digitalWrite(OLED_CS, LOW);

	// Send the command via SPI
	SPI.transfer(cmd);

	// Unassert CS
	digitalWrite(OLED_CS, HIGH);

	SPI.endTransaction();
}

void SSD1306::send_data(uint8_t data)
{
	SPI.beginTransaction(spi_config);

	// Set DC high, CS low for data
	digitalWrite(OLED_DC, HIGH);
	digitalWrite(OLED_CS, LOW);

	// Send the data via SPI
	SPI.transfer(data);

	// Unassert DC and CS
	digitalWrite(OLED_DC, HIGH);
	digitalWrite(OLED_CS, HIGH);

	SPI.endTransaction();
}

